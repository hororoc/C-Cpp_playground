#include <stdio.h>

int main(void) {
    printf("0x%08X\n", 0xFFFFFFFF & 0x00080000);
    // 1111 1111 1111 1111 1111 1111 1111 1111
    // 0000 0000 0000 1000 0000 0000 0000 0000
    // -> 0000 0000 0000 1000 0000 0000 0000 0000 (0x00080000)

    printf("0x%08X\n", 0xAAAABBBB & 0x00080000);
    // 1010 1010 1010 1010 1011 1011 1011 1011
    // 0000 0000 0000 1000 0000 0000 0000 0000
    // -> 0000 0000 0000 1000 0000 0000 0000 0000 (0x00080000)

    printf("0x%08X\n", 0xAABBCCDD & 0x00080000);
    // 1010 1010 1011 1011 1100 1100 1101 1101
    // 0000 0000 0000 1000 0000 0000 0000 0000
    // -> 0000 0000 0000 1000 0000 0000 0000 0000 (0x00080000)

    printf("0x%08X\n", 0xFFFFFFFF | 0x00080000);
    // 1111 1111 1111 1111 1111 1111 1111 1111
    // 0000 0000 0000 1000 0000 0000 0000 0000
    // -> 1111 1111 1111 1111 1111 1111 1111 1111 (0xFFFFFFFF)

    printf("0x%08X\n", 0x11223344 | 0x00080000);
    // 0001 0001 0010 0010 0011 0011 0100 0100
    // 0000 0000 0000 1000 0000 0000 0000 0000
    // -> 0001 0001 0010 1010 0011 0011 0100 0100 (0x112A3344)

    printf("0x%08X\n", 0x00000000 | 0x000F0000);
    // 0000 0000 0000 0000 0000 0000 0000 0000
    // 0000 0000 0000 1111 0000 0000 0000 0000
    // -> 0000 0000 0000 1111 0000 0000 0000 0000 (0x000F0000)

    return 0;
}