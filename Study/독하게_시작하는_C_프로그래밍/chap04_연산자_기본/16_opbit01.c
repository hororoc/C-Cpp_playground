#include <stdio.h>

int main(void) {
    int nData = 0x11223344; // 0001 0001 0010 0010 0011 0011 0100 0100
    printf("0x%08X\n",
           nData & 0x00FFFF00); // 0000 0000 1111 1111 1111 1111 0000 0000 ->
                                // 0000 0000 0010 0010 0011 0011 0000 0000
    printf("0x%08X\n",
           nData | 0x2211FFFF); // 0010 0010 0001 0001 1111 1111 1111 1111 ->
                                // 0011 0011 0011 0011 1111 1111 1111 1111
    printf("0x%08X\n",
           nData ^ 0x2211FFFF); // 0010 0010 0001 0001 1111 1111 1111 1111 ->
                                // 0011 0011 0011 0011 1100 1100 1011 1011
    printf("0x%08X\n", ~nData); // 1110 1110 1101 1101 1100 1100 1011 1011
    printf("0x%08X\n", nData >> 8);  // 0000 0000 0001 0001 0010 0010 0011 0011
    printf("0x%08X\n", nData << 16); // 0011 0011 0100 0100 0000 0000 0000 0000

    return 0;
}